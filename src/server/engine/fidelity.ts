/** 真实度档位 → 系统 Prompt（对齐产品文档：严格 / 演绎 / 架空） */
export function getFidelityBlock(fidelity: string): string {
  switch (fidelity) {
    case "strict":
      return [
        "## 真实度：严格史实（Strict）",
        "- 官职、称呼、制度与事件须贴近该时代常识；禁止引入未在场景档案、在场人物设定或检索史料中出现的可核查细节。",
        "- 明显不确定处仅在 strategyNote 中标注，勿在公开发言中编造具体年代、人名、官名。",
        "- 禁用现代网络梗、后世政治术语与明显时代错乱。",
      ].join("\n");
    case "fiction":
      return [
        "## 真实度：架空推演（Counterfactual）",
        "- 用户知悉此为「假如」式推演；可改变事件结果，但须保持该朝代政治文化与制度框架自洽。",
        "- 以戏剧冲突与议题推进为主；人物态度仍应与其档案标签大体一致，除非场景明确允许颠覆。",
      ].join("\n");
    default:
      return [
        "## 真实度：史实框架内演绎（Deductive，默认）",
        "- 在史实框架内允许合理推演；未见于史册的台词可以虚构，但须符合人物侧写与时代逻辑。",
        "- 大体符合职官与礼法；细节与对白可适度虚构以推进廷辩。",
      ].join("\n");
  }
}
